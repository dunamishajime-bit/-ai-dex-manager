function summaryRow(result: Awaited<ReturnType<typeof runHybridBacktest>>) {
    return {
        name: result.label,
        mode: result.mode,
        end_equity: Number(result.summary.end_equity.toFixed(2)),
        cagr_pct: Number(result.summary.cagr_pct.toFixed(2)),
        max_drawdown_pct: Number(result.summary.max_drawdown_pct.toFixed(2)),
        profit_factor: Number(result.summary.profit_factor.toFixed(3)),
        trade_count: result.summary.trade_count,
        annual_returns: result.summary.annual_returns,
    };
}

function renderComparisonMarkdown(rows: ReturnType<typeof summaryRow>[]) {
    const header = [
        "# Hybrid Benchmark Suite",
        "",
        "| Strategy | End Equity | CAGR | MaxDD | PF | Trades | 2023 | 2024 | 2025 |",
        "| --- | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: |",
    ];

    const body = rows.map((row) => {
        const byYear = new Map(row.annual_returns.map((item) => [item.period, item.return_pct]));
        return `| ${row.name} | ${row.end_equity.toFixed(2)} | ${row.cagr_pct.toFixed(2)}% | ${row.max_drawdown_pct.toFixed(2)}% | ${row.profit_factor.toFixed(3)} | ${row.trade_count} | ${(byYear.get("2023") || 0).toFixed(2)}% | ${(byYear.get("2024") || 0).toFixed(2)}% | ${(byYear.get("2025") || 0).toFixed(2)}% |`;
    });

    return [...header, ...body, ""].join("\n");
}

async function main() {
    const results = [];
    for (const variant of VARIANTS) {
        const result = await runHybridBacktest(variant.mode, variant.options);
        results.push(result);
    }

    const rows = results
        .map(summaryRow)
        .sort((left, right) => right.end_equity - left.end_equity);

    await fs.mkdir(REPORT_DIR, { recursive: true });
    await fs.writeFile(path.join(REPORT_DIR, "summary.json"), JSON.stringify(rows, null, 2), "utf8");
    await fs.writeFile(path.join(REPORT_DIR, "comparison.md"), renderComparisonMarkdown(rows), "utf8");

    console.log(JSON.stringify(rows, null, 2));
}

main().catch((error) => {
    console.error(error);
    process.exit(1);
});
